def main():
    init_knowledge_table()
    with _connect() as conn:
        existing = {r[0] for r in conn.execute("SELECT title FROM knowledge_items").fetchall()}

    inserted = 0
    skipped = 0
    now = datetime.now().isoformat()
    for r in RECORDS:
        if r["title"] in existing:
            print(f"  跳过（已存在）：{r['title']}")
            skipped += 1
            continue
        result = create_knowledge_item(
            title=r["title"],
            kind=r["kind"],
            symptom=r.get("symptom", ""),
            pattern_text=r.get("pattern_text", ""),
            harm=r.get("harm", ""),
            prevention=r.get("prevention", ""),
            images=[],
            status=1,
        )
        if result.get("code") == 200:
            print(f"  已插入：{r['title']}")
            inserted += 1
        else:
            print(f"  失败：{r['title']} → {result.get('message')}")

    print(f"\n完成：新增 {inserted} 条，跳过 {skipped} 条（已存在）。")


if __name__ == "__main__":
    main()
